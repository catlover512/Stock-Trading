                                    <h2>Điều 2. Giải thích từ ngữ</h2>
                                    <div>
                                        <ul>
                                            <li>2.1. “Dữ liệu cá nhân” là thông tin dưới dạng ký hiệu, chữ viết, chữ số,
                                                hình ảnh, âm thanh hoặc dạng tương tự trên môi trường điện tử gắn liền
                                                với một con người cụ thể hoặc giúp xác định một con người cụ thể.
                                                <br>Dữ liệu cá nhân bao gồm Dữ liệu cá nhân cơ bản và Dữ liệu cá nhân
                                                nhạy cảm.
                                            </li>
                                            <li>2.2. “Dữ liệu cá nhân cơ bản” bao gồm: </li>
                                            <ol>
                                                <p>a） Họ, chữ đệm và tên khai sinh, tên gọi khác (nếu có);</p>
                                                <p>b） Ngày, tháng, năm sinh;</p>
                                                <p>c） Giới tính;</p>
                                                <p>d） Nơi sinh, nơi đăng ký khai sinh, nơi thường trú, nơi tạm trú, nơi
                                                    ở hiện tại, quê quán, địa chỉ liên hệ; </p>
                                                <p>e） Quốc tịch;</p>
                                                <p>f） Hình ảnh của cá nhân;</p>
                                                <p>g） Số điện thoại, số chứng minh nhân dân, số định danh cá nhân, số hộ
                                                    chiếu, số giấy phép lái xe, số biển số xe, số mã số thuế cá nhân, số
                                                    bảo hiểm xã hội, số thẻ bảo hiểm y tế;</p>
                                                <p>h） Tình trạng hôn nhân;</p>
                                                <p>i） Thông tin về mối quan hệ gia đình (cha mẹ, con cái).</p>
                                                <p>j） Thông tin về tài khoản số của cá nhân; dữ liệu cá nhân phản ánh
                                                    hoạt động, lịch sử hoạt động trên không gian mạng;</p>
                                                <p>k） Các thông tin khác gắn liền với một con người cụ thể hoặc giúp xác
                                                    định một con người cụ thể mà không thuộc trường hợp Dữ liệu cá nhân
                                                    nhạy cảm.</p>
                                            </ol>

                                            <li>2.3. “Dữ liệu cá nhân nhạy cảm” là dữ liệu cá nhân gắn liền với quyền
                                                riêng tư của cá nhân mà khi bị xâm phạm sẽ gây ảnh hưởng trực tiếp tới
                                                quyền và lợi ích hợp pháp của cá nhân gồm: </li>
                                            <ol>
                                                <p>a） Quan điểm chính trị, quan điểm tôn giáo;</p>
                                                <p>b） Tình trạng sức khỏe và đời tư được ghi trong hồ sơ bệnh án, không
                                                    bao gồm thông tin về nhóm máu;</p>
                                                <p>c） Thông tin liên quan đến nguồn gốc chủng tộc, nguồn gốc dân tộc;
                                                </p>
                                                <p>d） Thông tin về đặc điểm di truyền được thừa hưởng hoặc có được của
                                                    cá nhân; </p>
                                                <p>e） Thông tin về thuộc tính vật lý, đặc điểm sinh học riêng của cá
                                                    nhân; </p>
                                                <p>f） Dữ liệu về tội phạm, hành vi phạm tội được thu thập, lưu trữ bởi
                                                    các cơ quan thực thi pháp luật;</p>
                                                <p>g） Thông tin khách hàng của tổ chức tín dụng, chi nhánh ngân hàng
                                                    nước ngoài, tổ chức cung ứng dịch vụ trung gian thanh toán, các tổ
                                                    chức được phép khác, gồm: thông tin định danh khách hàng theo quy
                                                    định của pháp luật, thông tin về tài khoản, thông tin về tiền gửi,
                                                    thông tin về tài sản gửi, thông tin về giao dịch, thông tin về tổ
                                                    chức, cá nhân là bên bảo đảm tại tổ chức tín dụng, chi nhánh ngân
                                                    hàng, tổ chức cung ứng dịch vụ trung gian thanh toán;</p>
                                                <p>h） Dữ liệu về vị trí của cá nhân được xác định qua dịch vụ định vị;
                                                </p>
                                                <p>i） Dữ liệu cá nhân khác được pháp luật quy định là đặc thù và cần có
                                                    biện pháp bảo mật cần thiết.</p>
                                            </ol>
                                            <li>2.4. “Xử lý dữ liệu cá nhân” là một hoặc nhiều hoạt động tác động tới dữ
                                                liệu cá nhân, như: thu thập, ghi, phân tích, xác nhận, lưu trữ, chỉnh
                                                sửa, công khai, kết hợp, truy cập, truy xuất, thu hồi, mã hóa, giải mã,
                                                sao chép, chia sẻ, truyền đưa, cung cấp, chuyển giao, xóa, hủy dữ liệu
                                                cá nhân hoặc các hành động khác có liên quan.</li>
