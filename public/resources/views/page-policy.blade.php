                                    <h2> Điều 4. Quyền và nghĩa vụ của Chủ thể dữ liệu liên quan đến Dữ liệu cá nhân
                                        cung cấp cho LUXSE </h2>
                                    <div>
                                        <ul>
                                            <li>4.1. Chủ thể dữ liệu có các quyền sau đây: (i) Quyền được biết; (ii)
                                                Quyền đồng ý; (iii) Quyền truy cập; (iv) Quyền rút lại sự đồng ý; (v)
                                                Quyền xóa dữ liệu; (vi) Quyền hạn chế xử lý dữ liệu; (vii) Quyền cung
                                                cấp dữ liệu; (viii) Quyền phản đối xử lý dữ liệu; (ix) Quyền khiếu nại,
                                                tố cáo, khởi kiện; (x) Quyền yêu cầu bồi thường thiệt hại; (xi) Quyền tự
                                                bảo vệ và các quyền có liên quan khác theo quy định của pháp luật. Chủ
                                                thể dữ liệu có thể thực hiện các quyền của mình bằng cách liên hệ với
                                                LUXSE theo thông tin được cung cấp chi tiết tại Điều 8 Điều khoản và
                                                Điều kiện về BVDLCN.</li>
                                            <li>4.2. LUXSE bằng sự nỗ lực hợp lý, sẽ thực hiện yêu cầu hợp pháp và hợp
                                                lệ từ Chủ thể dữ liệu trong khoảng thời gian luật định kể từ khi nhận
                                                được yêu cầu hoàn chỉnh, hợp lệ và phí xử lý liên quan (nếu có) từ Chủ
                                                thể dữ liệu, phụ thuộc vào quyền của LUXSE khi được viện dẫn đến bất kỳ
                                                sự miễn trừ và/hoặc ngoại lệ nào theo quy định pháp luật. </li>
                                            <li>4.3. Trong trường hợp Chủ thể dữ liệu rút lại sự đồng ý của mình, yêu
                                                cầu xóa dữ liệu và/hoặc thực hiện các quyền có liên quan khác đối với
                                                bất kỳ hoặc tất cả các Dữ liệu cá nhân của mình, và tuỳ thuộc vào bản
                                                chất yêu cầu của Chủ thể dữ liệu, LUXSE có thể sẽ xem xét và quyết định
                                                về việc không tiếp tục cung cấp các sản phẩm, dịch vụ của LUXSE có liên
                                                quan đến việc sử dụng Dữ liệu cá nhân của Chủ thể dữ liệu do không thể
                                                đảm bảo tiêu chuẩn/chất lượng của sản phẩm, dịch vụ theo đánh giá của
                                                LUXSE hoặc do quy định của pháp luật cần phải thu thập Dữ liệu cá nhân
                                                của Chủ thể dữ liệu khi cung cấp sản phẩm, dịch vụ. Trong trường hợp
                                                này, LUXSE sẽ thông báo đến Khách hàng về quyết định không cung cấp sản
                                                phẩm, dịch vụ có nêu rõ lý do. Mọi thiệt hại phát sinh đối với Khách
                                                hàng và LUXSE (nếu có) sẽ do Chủ thể dữ liệu hoàn toàn chịu trách nhiệm.
                                                Chủ thể dữ liệu cần lưu ý, do đặc thù hoạt động của LUXSE, pháp luật có
                                                quy định LUXSE phải lưu trữ thông tin Chủ thể dữ liệu trong một số
                                                trường hợp nhất định, khi đó LUXSE không thể đáp ứng yêu cầu xóa dữ liệu
                                                của Chủ thể dữ liệu có liên quan nếu việc xóa dữ liệu dẫn đến vi phạm
                                                pháp luật.</li>
                                            <li>4.4. Vì mục đích bảo mật, Chủ thể có thể cần phải đưa ra yêu cầu của
                                                mình bằng văn bản hoặc sử dụng phương pháp khác để chứng minh và xác
                                                thực danh tính của Chủ thể dữ liệu. LUXSE có thể yêu cầu Chủ thể dữ liệu
                                                xác minh danh tính trước khi xử lý yêu cầu của Chủ thể dữ liệu.</li>
                                            <li>4.5. Chủ thể dữ liệu có trách nhiệm tự bảo vệ Dữ liệu cá nhân của mình,
                                                yêu cầu các tổ chức, cá nhân khác có liên quan bảo vệ Dữ liệu cá nhân
                                                của mình. Đồng thời, Chủ thể dữ liệu sẽ tôn trọng và bảo vệ Dữ liệu cá
                                                nhân của người khác.</li>
                                            <li>4.6. Cung cấp đầy đủ, chính xác Dữ liệu cá nhân cho LUXSE khi giao kết
                                                hợp đồng hoặc sử dụng dịch vụ do LUXSE cung cấp.</li>
                                            <li>4.7. Thực hiện và tuân thủ quy định của pháp luật về bảo vệ Dữ liệu cá
                                                nhân và tham gia phòng, chống các hành vi vi phạm quy định về bảo vệ Dữ
                                                liệu cá nhân.</li>
                                            <li>4.8. Trong trường hợp có sự thay đổi, điều chỉnh Dữ liệu cá nhân, Chủ
                                                thể dữ liệu và/hoặc bên có liên quan có trách nhiệm liên hệ và thông báo
                                                ngay cho LUXSE để LUXSE thực hiện cập nhật kịp thời những thay đổi, điều
                                                chỉnh đó. Chủ thể dữ liệu và/hoặc bên có liên quan sẽ chịu trách nhiệm
                                                toàn bộ đối với việc chậm trễ thông báo này; đồng thời, việc chậm trễ
                                                thông báo này sẽ miễn trừ cho LUXSE khỏi mọi thiệt hại, rủi ro phát sinh
                                                (nếu có).</li>

