                                                tin cá nhân của Chủ thể dữ liệu mà không có sự chấp thuận của Chủ thể dữ
                                                liệu theo pháp luật hiện hành. Tuy nhiên, để thực hiện các mục đích và
                                                hoạt động xử lý Dữ liệu cá nhân tại Điều khoản và điều kiện về BVDLCN
                                                này, Chủ thể dữ liệu đồng ý LUXSE có thể tiết lộ Dữ liệu cá nhân của
