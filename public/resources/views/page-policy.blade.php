                                            <ol>
                                                <p>a）Khi có sự đồng ý của Chủ thể dữ liệu; </p>
                                                <p>b）Khi LUXSE được yêu cầu hoặc được phép tiết lộ theo quy định pháp
                                                    luật; hoặc theo quyết định của cơ quan nhà nước có thẩm quyền; </p>
