                                            <p>- 3.2.2. LUXSE sẽ yêu cầu sự cho phép từ Chủ thể dữ liệu trước khi sử
                                                dụng Dữ liệu cá nhân của Chủ thể dữ liệu cho mục đích khác ngoài các mục
