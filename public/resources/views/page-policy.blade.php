                                                dịch vụ do LuxSE cung cấp.</li>
                                            <li>2.7. “Công ty” hoặc “LuxSE” là Công ty cổ phần chứng khoán LuxSE, bao
                                                gồm trụ sở, chi nhánh, văn phòng, phòng giao dịch của công ty (nếu có).
                                            </li>
                                            <li>2.8. “Bên thứ ba” là tổ chức, cá nhân ngoài LuxSE và Chủ thể dữ liệu.
                                                <br>Để làm rõ, các từ ngữ nào chưa được giải thích tại Điều khoản và
                                                điều kiện chung về BVDLCN này sẽ được giải thích theo quy định của pháp
                                                luật Việt Nam.
                                            </li>

                                        </ul>
